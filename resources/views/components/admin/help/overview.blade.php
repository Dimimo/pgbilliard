        A birdseye view of the website structure, hierarchy and functions:
    </div>
    <div>
        <span class="font-bold">Before starting a new Season</span>, add any new Venue that may have joined the
        League. You can add a Venue later, but it will complicate the workflow of creating a new Season and its
        Calendar. You can create a new player (in this case, a captain) on the fly.
    </div>
    <div>
        Only admins can manipulate anything Calendar based. Bar owners have access to their Venue, Teams, Captains
        and Players. Captains have access to the player list of their team and can appoint other captains.
    </div>
    <div>
        During the game (now on Wednesdays), every player participating in <span class="font-bold">their game</span>
        (Events, as I call it in the database) has access to the score of the game they are playing.
        From 12:00 to 20:00 as it is now.
    </div>
    <div>
        When <span class="font-bold">all games are confirmed</span>, an email will be sent to the participating
        players with the end results of the day.
    </div>
    <div>
        The Scoresheet (ranking overview) itself is immutable. So are daily end-scores that are confirmed.
