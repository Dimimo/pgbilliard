    <h3 class="mb-2 text-xl font-bold">
        <a id="a__8_0_General_Pool_Rules"></a>8.0 General Pool Rules
    </h3>

    <div class="mb-4 rounded-lg border-2 border-gray-600 bg-gray-100 p-2">
        <h4 class="mb-2 text-lg font-bold">
            <a id="a__8_1_Starting_Times"></a>8.1 Starting Times
        </h4>

        <div class="mb-4">
            The starting times for all matches is <span class="font-bold">2:00 PM (14:00 hrs)</span>. The scoresheet must be filled out by each team
            by
            <span class="font-bold">2:00 PM</span>, with only the names of players that are at the venue and ready to play.
            If any player arrives after the start time, he/she is to be placed in the next available slot. Providing that a female
            player is in the starting <span class="font-bold">position</span>.
        </div>
        <div class="mb-4">
            If the 2 captains prefer and <span class="font-bold">both agree</span>, starting time and day of the week can be changed.
        </div>

        <h4 class="mb-2 text-lg font-bold">
            <a id="a__8_2_Teams"></a>8.2 Teams
        </h4>

        <div class="mb-4">
            A team consists of at least <span class="font-bold">4 players</span>. The team must consist of at
            least one female player, who can be either an <span class="font-bold">Expat</span> or
            <span class="font-bold">Filipino</span>. If the starting four consists of more than one
            <span class="font-bold">Filipino</span> player, they must play in the same position throughout the match.
        </div>
        <div class="mb-4">
            The remaining two places must be filled by an <span class="font-bold">Expat</span>, the exception being if one or
            more is a female player, then they can be <span class="font-bold">Filipinos</span>.
        </div>
        <div class="mb-4">
            A team can consist of <span class="font-bold">4 players plus inter-change players</span>
            who can play a maximum of <span class="font-bold">4 games each</span>, only after the first four single games. The female player in
            the starting four must play at least <span class="font-bold">two games</span>.
        </div>

        <h4 class="mb-2 text-lg font-bold">
            <a id="a__8_3_Forfeits"></a>8.3 Forfeits
        </h4>

        <div>
            Any team that forfeits a match will receive <span class="font-bold">no game wins</span> for the match
            and <span class="font-bold">eight game losses</span>. The opposing teamâ€™s score will be a win of
            <span class="font-bold">8/0</span> or <span class="font-bold">0/8</span> respectively.
        </div>
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__8_4_Wrong_Balls_Pocketed"></a>8.4 Wrong Balls Pocketed
    </h4>

    <div class="mb-4">
        When it is discovered that the shooting player has been <span class="font-bold">shooting the opponent's designated
        balls as if they were their own</span>, the shooter will relinquish the table, and the opponent will receive
        <span class="font-bold">ball in hand</span>.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__8_5_Split_Hits"></a>8.5 Split Hits
    </h4>

    <div class="mb-4">
        If the cue ball strikes an object ball and a <span class="font-bold">non-legal object ball</span>
        around the same instant and it cannot be clearly determined which ball was hit first, the decision will go in favor of the
        <span class="font-bold">shooter</span>.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__8_6_Ball_Rebounds_From_Pocket"></a>8.6 Ball Rebounds From Pocket
    </h4>

    <div class="mb-4">
        Balls must <span class="font-bold">remain in the pocket</span> to be counted as pocketed. If it is the
        <span class="font-bold">8-ball</span>, it is <span class="font-bold">not a win</span>. If it is the cue ball, it is
        <span class="font-bold">not a scratch</span>. <span class="font-bold">Clearing the pockets</span> that are full or
        nearly full is the responsibility of the <span class="font-bold">shooting player</span>.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__8_7_Hanging_Ball"></a><span class="font-bold">8.7 Hanging Ball</span>
    </h4>

    <div class="mb-4">
        If an object ball <span class="font-bold">hangs</span> in the pocket and drops within
        <span class="font-bold">5 seconds</span> or less after coming to rest by the hole, the ball is considered to be pocketed. If a hanging
        ball drops into the pocket <span class="font-bold">after 5 seconds</span>, the ball is to be returned to its
        <span class="font-bold">original position</span>.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__8_8_Suspended_Ball"></a><span class="font-bold">8.8 Suspended Ball</span>
    </h4>

    <div class="mb-4">
        If one or more balls become <span class="font-bold">suspended</span> in a pocket beyond the edge of
        the slate because it is suspended by balls already in the pocket, it is <span class="font-bold">considered pocketed</span> if the
        removal of the pocketed balls would cause it to fall into the pocket.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__8_9_Jump_Shots"></a><span class="font-bold">8.9 Jump Shots</span>
    </h4>

    <div class="mb-4">
        Jump shots are <span class="font-bold">not allowed</span>.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__8_10_Score_Sheet"></a><span class="font-bold">8.10 Score Sheet</span>
    </h4>

    <div class="mb-4">
        It is the responsibility of the <span class="font-bold">home team captain</span> to
        <span class="font-bold">keep score</span> on the provided scoresheet and to make sure that all players play in their assigned
        positions.<br>The final score must be <span class="font-bold">texted to the Admin on completion of the game</span>.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__8_11_Protesting_A_Foul"></a><span class="font-bold">8.11 Protesting A Foul</span>
    </h4>

    <div class="mb-4">
        If an incoming player <span class="font-bold">believes the shooter has committed a ball in hand foul</span>
        and the shooter denies it, the <span class="font-bold">two captains</span> are to make the call on the outcome.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__8_12_Resolving_Disputes"></a><span class="font-bold">8.12 Resolving Disputes</span>
    </h4>

    <div class="mb-4">
        Any disputes that may arise <span class="font-bold">during or after a game</span> or match should be
        referred to the <span class="font-bold">Match Committee</span> for clarification.<br>
        Only the <span class="font-bold">Captain of a side</span> is allowed to submit a protest or request a rule clarification.
    </div>

