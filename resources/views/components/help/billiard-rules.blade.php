    <h3 class="mb-2 text-xl font-bold">
        <a id="a__10_0_Other_Rules_-_PGPL"></a><span class="font-bold">10.0 Other Rules - PGPL</span>
    </h3>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__10_1_Frozen_Object_Ball_To_Cue_Ball"></a><span class="font-bold">10.1 Frozen Object Ball To Cue Ball</span>
    </h4>

    <div class="mb-4">
        In the case of a <span class="font-bold">cue ball frozen to an object ball</span> (player's own ball),
        the player can play <span class="font-bold">away from the ball</span> and is <span class="font-bold">not required to hit another of his object balls</span>.<br>He
        can hit an <span class="font-bold">opposition ball without penalty</span>, but at least <span
            class="font-bold">one ball must hit a rail</span>
        in its play.
    </div>

    <div class="mb-4">
        The <span class="font-bold">frozen object ball</span> is <span class="font-bold">allowed to move</span>
        in this case, but the player <span class="font-bold">cannot play a direct hit on the ball</span>â€”this is termed as a
        <span class="font-bold">push shot</span> and will <span class="font-bold">incur a penalty of ball in hand</span>.
    </div>
    <h4 class="mb-2 text-lg font-bold">
        <a id="a__10_2_Frozen_Opposition_Ball_To_Cue_Ball"></a><span class="font-bold">10.2 Frozen Opposition Ball To Cue Ball</span>
    </h4>

    <div class="mb-4">
        In the case of a <span class="font-bold">cue ball frozen to an opposition ball</span>, the
        <span class="font-bold">object ball must not move</span>.<br>If it does, it is a <span class="font-bold">ball in hand penalty</span>.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        10.3 Deliberate Foul On The 8 Ball
    </h4>

    <div class="mb-4">
        Any deliberate shot on the 8-ball while the player has object balls on the table is an intentional foul and will
        result in loss of game.
        <span class="font-bold">Example:</span> If a player plays the 8-ball onto his own ball with the intention of
        sinking it or improving its position, this results in loss of game.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        10.4 Submitting A Protest
    </h4>

    <div class="mb-4">
        In the case of a dispute regarding a rule that could cause loss of game, and if the two captains cannot come to an agreement, the issue can be
        referred to the Match Committee for a ruling.
    </div>

    <div class="mb-4">
        The protest must be submitted on the same day the match is completed.
        The captain of the protesting side must inform the opposing captain that he is submitting a protest.
        Protests submitted the next day will not receive a hearing, and any decision made on the day of play will stand.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        11.0 Finals
    </h4>

    <div class="mb-4">
        At the end of the Qualifying Matches, the final four teams are determined by the teams with the most wins (points scored).
    </div>

    <div class="mb-4">
        If two teams have the same number of wins, the winner of the last match between those two teams will take the higher spot.
        Percentages will only come into play if the finishing order cannot be decided by the above method.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        Semi-Final & Grand Final Format:
    </h4>

    <ul class="mb-4 list-inside list-disc">
        <li>The first team plays the fourth team in the 1st Semi-Final</li>
        <li>The second team plays the third team in the 2nd Semi-Final</li>
        <li>Team 1 and Team 2 play at their chosen venue</li>
        <li>The Grand Final is played at the venue of Team 1 if they win the 1st Semi-Final</li>
        <li>If Team 4 wins the Semi-Final, the Grand Final is played at the venue of the winner of the 2nd Semi-Final (Teams 2 & 3)</li>
    </ul>

    <hr>
    <h3 class="mb-2 text-xl font-bold">
        12.0 Substitute Players
    </h3>

    <div class="mb-4">
        If a team does not have enough players for a match, whether it is a qualifying match or a finals match.
        The team can ask the other captain for assistance in filling the team.
    </div>

    <div class="mb-4">
        In doing so, they must use only their own qualified team members in the starting four.
    </div>

    <hr>
    <h3 class="mb-2 text-xl font-bold">
        13.0 Stalemate
    </h3>

    <div class="mb-4">
        If after 3 consecutive turns at the table by each player (6 turns in total), the referee judges
        (or if there is no referee, both players agree) that attempting to pocket or move an object ball
        will result in loss of game, the balls are to be re-racked with the original player
        performing the break. The Stalemate Rule may only be used when there are only two or fewer object
        balls and the 8-ball remaining on the table.
    </div>

    <div class="mb-4">
        <span class="font-bold text-red-700">Please Note:</span> Three consecutive fouls by one player is NOT a loss of game.
    </div>
</div>
