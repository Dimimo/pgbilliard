    <h3 class="mb-2 text-xl font-bold">
        <a id="a__7_0_Loss_Of_Game"></a>7.0 Loss Of Game
    </h3>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__7_1_Opponent_Wins"></a>7.1 Opponent Wins
    </h4>

    <div class="mb-4">
        The opponent wins after legally pocketing the <span class="font-bold">8-ball</span>.
    </div>

    <div class="mb-4">
        <span class="font-bold">HOWEVER:</span>
        <ul class="list-inside list-disc">
            <li>
                If the player pockets the <span class="font-bold">8-ball</span> and an <span class="font-bold">opponentâ€™s ball</span>
                at the same time, it is a <span class="font-bold">win</span> providing the opposition had more than one ball on the
                table.
            </li>
            <li>
                If the
                opposition has <span class="font-bold">only one ball left on the table</span>, and that ball is pocketed along with the
                <span class="font-bold">8-ball</span>, <span class="font-bold">it is a loss</span> to the player that pockets
                both balls.
            </li>
        </ul>
    </div>


    <h4 class="mb-2 text-lg font-bold">
        <a id="a__7_2_8-Ball_Foul"></a>7.2 8-Ball Foul
    </h4>

    <div class="mb-4">
        An <span class="font-bold">8-ball</span> foul occurs when the <span class="font-bold">8-ball</span>
        comes to rest off the pool table, when the <span class="font-bold">8-ball</span> is pocketed in the wrong pocket or out of sequence,
        or when the <span class="font-bold">8-ball</span> is pocketed while a foul occurred, i.e., the player scratches at the same time as
        pocketing the <span class="font-bold">8-ball</span>.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__7_3_Illegal_Hit_On_8-Ball"></a>7.3 Illegal Hit On 8-Ball
    </h4>

    <div class="mb-4">
        An illegal hit on the <span class="font-bold">8-ball</span> to cause a snooker to the opposition
        player results in loss of game.
    </div>

