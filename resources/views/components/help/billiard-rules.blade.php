    <h3 class="mb-2 text-xl font-bold">
        <a id="a__1_0_Object_of_the_Game"></a>1.0 Object of the Game
    </h3>

    <div class="mb-4">
        8-ball is played with a cue ball and fifteen object balls numbered 1 to 15. 1 to 7 are solid colours and are
        commonly called low, small, or solid balls. 9 to 15 are striped and are commonly called large balls. One player must play the solid balls
        while
        the other player must pocket the large balls. The player that pockets all their entire group and then legally pockets the 8-ball wins the
        game.
    </div>

    <h3 class="mb-2 text-xl font-bold">
        <a id="a__2_0_Legal_Break_Shot"></a>2.0 Legal Break Shot
    </h3>

    <div class="mb-4">For the break shot to be legal, the breaker (with the cue ball placed anywhere behind the head string) commonly
        called the kitchen, must either pocket a numbered ball or drive at least 4 racked balls to one or more rails. If the breaker fails to make a
        legal
        break, the balls will be re-racked and the opposition player has the right to perform the break, or ask the offending player to re-break.
    </div>

    <h3 class="mb-2 text-xl font-bold">
        <a id="a__3_0_8_Ball_Pocketed_On_The_Break_"></a>3.0 8-ball Pocketed On The Break.
    </h3>

    <div class="mb-4">If the eight-ball is pocketed on the break, the opponent has the option of re-racking the balls for a re-start, or
        spotting the black (if this option is taken, the cue ball remains in the position it came to rest and is played from said position). The
        player
        that broke will break again, or if the eight-ball is respotted and no other ball has been pocketed the opposition player takes the next shot.
    </div>
    <h3 class="mb-2 text-xl font-bold"><a id="a__4_0_Open_Table_"></a>4.0 Open Table.</h3>

    <div class="mb-4">
        The table is always open immediately after the break shot. The player's designated group (solid or stripes) will not
        be determined until a player legally pockets a called object ball. The table is considered "Open" until an object ball is legally pocketed.
        When
        the table is open it is legal to hit one group of balls in order to pocket a ball from the opposite group.<br>
        <span class="font-bold">Note:</span> The 8-ball may be utilized in a combination as long as it is not hit first, if it is, it is a
        foul shot.
    </div>

    <h3 class="mb-2 text-xl font-bold">
        <a id="a__5_0_Call_Pocket_"></a>5.0 Call Pocket
    </h3>

    <div class="mb-4">
        It is encouraged that all balls be specified along with their intended pocket. However, obvious balls and their
        respective pockets do not have to be specified. Any bank shot (object ball to rail) kick shot (rail or rails to object ball), or combinations
        (2
        or more balls from either group) must be called to their designated pocket or they are called a miss.
    </div>

    <div class="mb-4">
        (A) A ball <span class="font-bold">does</span> have to hit a rail if the player is snookered.<br>
        (B) It is <span class="font-bold">never necessary</span> to specify details such as the number of banks, rails, or kisses, etc.<br>
        (C) A ball must be pocketed or strike a rail after contact with the cue ball.<br>
        (D) Any balls pocketed, legally or illegally, as the result of a called shot will remain pocketed regardless of the
        group (stripes or solids).
    </div>

    <h3 class="mb-2 text-xl font-bold">
        <a id="a__6_0_Ball_In_Hand_Penalties_"></a>6.0 Ball In Hand Penalties.
    </h3>

    <div class="mb-4">
        Touching or causing even the slightest movement of the cue ball (other than a normal shot) even accidentally is a
        foul. However, a player may use the ferrule or shaft of the cue to line up the cue ball when a "ball in hand" is in play. Using the tip to do
        so
        is a foul and ball in hand is given to the other player.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__6_1_Scratch"></a>6.1 Scratch
    </h4>

    <div class="mb-4">
        Pocketing the cue ball or driving it off the table is a <span class="font-bold">ball in hand</span>
        foul. If a scratch occurs while shooting the <span class="font-bold">8-ball</span> but was not pocketed or removed from the table
        the game continues with ball in hand to the opponent (scratching on the <span class="font-bold">8-ball</span> is <span
            class="font-bold">not</span> loss of game as long as the <span class="font-bold">8-ball</span> is still in play).
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__6_2_No_Rail"></a>6.2 No Rail
    </h4>

    <div class="mb-4">If after the cue ball strikes a legal ball and neither the cue ball nor any other ball fails to hit a rail or is
        pocketed, it is a ball in hand foul. A "Frozen" (touching) object ball to the rail does not meet this requirement.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__6_3_Balls_Off_The_Table"></a>6.3 Balls Off The Table
    </h4>

    <div class="mb-4">
        Causing any ball to fly off the table is a foul, and any such ball is pocketed, the opposing player is next to play.
        Exception is the <span class="font-bold">8-ball</span>; if the <span class="font-bold">8-ball</span> is removed from
        the table at any time, then it becomes <span class="font-bold">loss of game</span>. (The exception to this is if the
        <span class="font-bold">8-ball</span> is stuck off the table on the break, then the opposing player has the option of re-spotting it
        or re-racking.)<br>If a player knocks a ball off the table and the ball returns to the table after hitting a person or object, the said ball
        remains on the surface of the table where it stops and is considered as a ball in hand foul.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__6_4_Jump_Shot"></a>6.4 Jump Shot
    </h4>

    <div class="mb-4">
        <span class="font-bold">NO jump shots are allowed.</span>
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__6_5_Double_Hit"></a>6.5 Double Hit
    </h4>

    <div class="mb-4">
        If the cue tip strikes the cue ball twice on the same stroke, it is a ball in hand foul.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__6_6_Head_String"></a>6.6 Head String
    </h4>

    <div class="mb-4">
        The base of the cue ball must be behind the head string on the break, or it is a ball in hand foul. A scratch on the
        break is a <span class="font-bold">ball in hand penalty</span>, and the opposing player must take the shot behind the
        headstring (aka, inside the 'kitchen') and <span class="font-bold">shoot forwards only</span>.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__6_7_Ball_In_Hand_Placement"></a>6.7 Ball In Hand Placement
    </h4>

    <div class="mb-4">
        Touching an object ball in any way while placing the cue ball is a ball in hand foul.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__6_8_Interference"></a>6.8 Interference
    </h4>

    <div class="mb-4">
        While the shooting player is at the table, <span class="font-bold">the non-shooting player</span>, as
        well as their teammates, cannot disturb, make noises, move around, or <span class="font-bold">cause distraction</span> in any way.
        All players must conduct themselves in a respectful manner.<br>At no time can a teammate suggest a certain shot to play in any singles game.
        In a
        <span class="font-bold">doubles</span> game, only the partner of a player can coach.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__6_9_Marking_The_Table"></a>6.9 Marking The Table
    </h4>

    <div class="mb-4">
        Marking the table in any way that could <span class="font-bold">provide an advantage</span> in
        executing a shot is a foul. This includes placing the chalk cube in a specific place on the edge of the table. The offending mark must be
        removed
        before the shot is taken, otherwise, it is a foul.
    </div>

    <h4 class="mb-2 text-lg font-bold">
        <a id="a__6_10_Playing_Out_Of_Turn"></a>6.10 Playing Out Of Turn
    </h4>

    <div class="mb-4">
        Playing out of turn is a foul. <span class="font-bold">Ball in hand</span> is to be taken by the
        opposition player.
    </div>

