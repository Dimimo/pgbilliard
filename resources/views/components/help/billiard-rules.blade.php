    <h3 class="mb-2 text-xl font-bold">
        <a id="a__9_0_Coaching"></a><span class="font-bold">9.0 Coaching</span>
    </h3>

    <div class="mb-4">
        It is the <span class="font-bold">aim of the league</span> to encourage as many new players as
        possible to take up playing pool.<br>In general, <span class="font-bold">no coaching is allowed in all singles games</span> and
        only between the <span class="font-bold">two players in doubles games</span>.
    </div>

    <div class="mb-4">
        However, if the <span class="font-bold">two captains</span> agree that a
        <span class="font-bold">particular player is a newcomer</span> to the sport and <span class="font-bold">agree to coaching that player</span>
        in a singles game, then the captain can coach or <span class="font-bold">designate a player from his team</span> to be the
        <span class="font-bold">one and only coach</span> for said player.
    </div>
    <div class="mb-4">
        Please refrain from <span class="font-bold">having more than one person giving advice</span>, as it
        <span class="font-bold">only causes confusion</span> for a new player and is <span class="font-bold">not allowed</span>.
    </div>

